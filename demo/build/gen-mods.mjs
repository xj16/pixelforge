// Generates demo/engine/mods.generated.js by inlining the repo's real bundled
// mods (mods/*/mod.json + main.lua) so the browser demo runs the SAME scripts
// the Godot game ships — no divergent copies. Run: node demo/build/gen-mods.mjs
import { readFileSync, writeFileSync, readdirSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repo = join(here, "..", "..");
const modsDir = join(repo, "mods");
const out = join(repo, "demo", "engine", "mods.generated.js");

const mods = [];
for (const name of readdirSync(modsDir)) {
  const dir = join(modsDir, name);
  const manifestPath = join(dir, "mod.json");
  if (!existsSync(manifestPath)) continue;
  const manifestJson = readFileSync(manifestPath, "utf8");
  const entry = JSON.parse(manifestJson).entry || "main.lua";
  const luaSource = readFileSync(join(dir, entry), "utf8");
  mods.push({ folder: name, manifestJson, luaSource });
}

// Stable order: sort by folder name.
mods.sort((a, b) => a.folder.localeCompare(b.folder));

const banner = `// AUTO-GENERATED by demo/build/gen-mods.mjs — do not edit by hand.
// Inlines the repo's real bundled mods so the browser demo runs the exact same
// Lua the Godot game ships. Regenerate after editing anything under mods/.
`;

const body =
  "export const BUNDLED_MODS = " +
  JSON.stringify(
    mods.map((m) => ({ manifestJson: m.manifestJson, luaSource: m.luaSource, origin: "builtin" })),
    null,
    2
  ) +
  ";\n";

writeFileSync(out, banner + "\n" + body);
console.log(`Wrote ${out} with ${mods.length} bundled mods: ${mods.map((m) => m.folder).join(", ")}`);
